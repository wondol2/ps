void solve(){
    int N; cin >> N;

    vector<int> v;
    while(N){
        v.push_back(N%9);
        N/=9;
    }
    for(int i=v.size()-1; i>=0; --i){
        cout << v[i];
    }
    cout << "\n";
}


int main(){
    ios::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    cin.exceptions(cin.failbit);
    int T=1;
    // cin >> T; // cin.ignore(256, '\n');
    while(T--){ solve(); }
    return 0;
}
