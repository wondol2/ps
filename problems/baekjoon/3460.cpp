void solve(){
    int N; cin >> N;

    int idx = 0;
    while(N){
        if(N&1) cout << idx << " ";
        N >>= 1;
        ++idx;
    }
    cout << "\n";
}


int main(){
    ios::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    cin.exceptions(cin.failbit);
    int T=1;
    cin >> T; // cin.ignore(256, '\n');
    while(T--){ solve(); }
    return 0;
}
