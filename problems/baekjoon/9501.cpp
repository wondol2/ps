void solve(){
    int N, D; cin >> N >> D;
    int ans = 0;
    while(N--){
        double v, f, c; cin >> v >> f >> c;

        if(D <= v*f/c) ++ans;
    }
    cout << ans << "\n";
}


int main(){
    ios::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    cin.exceptions(cin.failbit);
    int T=1;
    cin >> T; // cin.ignore(256, '\n');
    while(T--){ solve(); }
    return 0;
}
